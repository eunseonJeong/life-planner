export const ALL_REGION_CODES: RegionCode[] = [
  ...SEOUL_REGION_CODES,
  ...GYEONGGI_REGION_CODES
]

// 지역 코드로 지역 정보 찾기
export function findRegionByCode(bjdCode: string): RegionCode | undefined {
  return ALL_REGION_CODES.find(region => region.bjdCode === bjdCode)
}

// 지역명으로 지역 코드 찾기
export function findRegionByName(name: string): RegionCode | undefined {
  return ALL_REGION_CODES.find(region => 
    region.fullName.includes(name) || 
    region.sigungu.includes(name) ||
    region.sido.includes(name)
  )
}
